#include "libft.h"

char	*ft_strchr(const char *s, int c)
/*
s – указатель на строку, в которой будет осуществляться поиск.
c – код искомого символа
*/

// Функция strchr ищет первое вхождения символа, код которого указан в аргументе c, в строке, на которую указывает аргумент s.
{
	int a;

	a = 0;
	if ( c == '\0')
	{
		while(s[a] != '\0')
			a++;
		return ((char *)&s[a]);
	}
	while(s[a] != '\0')
	{
		if ( s[a] == c )
			return ((char *)&s[a]);
		a++;
	}
	return 0;
}

